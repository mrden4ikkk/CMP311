#!/bin/bash

# Поточний користувач
echo "Поточний користувач: $(whoami)"

# Назва операційної системи та її версія
echo "Операційна система та її версія: $(lsb_release -d | cut -f2)"

# Кількість вільного місця на диску
echo "Вільне місце на диску:"
df -h | grep '^/dev/'

# Список активних користувачів
echo "Список активних користувачів:"
who
